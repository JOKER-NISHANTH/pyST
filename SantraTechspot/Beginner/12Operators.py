# Arithmetic Operator
# + - / % // * **
print("\t Arithmetic Operator ")
print(5+2)
print(5-2)
print(5/2)
print(5%2)
print(5//2)
print(5 ** 2)

# Relation Operator  Return Boolean
print("\n \t Relation Operator ")
print(2==3)
print(2==2)
print(2!=3)
print(2!=2)
print(2<=3)
print(2 >= 3)
print(2 == 3)
print(2 < 3)
print(2 > 3)

# Assignment Operator
print("\n \t Assignment Operator ")
a = 10
a += 5
print(a)

# Logical Operator
print("\n \t Logical Operator ")
print((2<3) and (4<5))
print((2<3) and (4<2))
print((2<3) or (4<2))
print((2 < 1) or (4 < 2))
print(not (2 < 1))
print(not(2==2))
