#================================= In 2.x raw_input() or input() ====================================
a = raw_input("Enter the a Value : ")
print(a, " ", type(a))  # String
b = input("Enter the a Value : ")
print(b, " ", type(b))  # Int

#================================= In 3.x input()  --> Only =======================================
b = input("Enter the a Value : ")
print(b, " ", type(b))  # Always String
